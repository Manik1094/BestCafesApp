package com.example.manikgupta.bestcafes;



public class CustomState {

    private String mName;
    private int mImages;

    public CustomState(String name , int images){

        this.mName  = name;
        this.mImages = images;
    }

    public String getmName() {
        return mName;
    }

    public int getmImages() {
        return mImages;
    }
}
